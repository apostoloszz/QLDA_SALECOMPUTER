    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int index = tblSanPham.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dòng");
        } else {
            String ma = (String) tblSanPham.getValueAt(index, 0);
            if (ss.update(ma, readForm()) > 0) {
                fillTable(ss.getAll());
                JOptionPane.showMessageDialog(this, "Sửa thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Sửa không thành công");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        String tim=txt_Tim.getText();
        if(tim.isEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập thông tin");
        }else{
            tim="%"+tim+"%";
            if(ss.serch(tim).size()>0){
                this.fillTable(ss.serch(tim));
                JOptionPane.showMessageDialog(this, "Tìm thành công");
            }else{
                JOptionPane.showMessageDialog(this, "Tìm thất bại");
            }
        }
    }//GEN-LAST:event_btnTimActionPerformed

