    void fillTable(List<QLSanPham> list) {
        model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);
        for (QLSanPham sp : list) {
            model.addRow(sp.toDataRow());
        }

    }

    QLSanPham readForm() {
        String ma, ten, maNsx;
        double gia;
        int soluong;
        ma = txtMa.getText();
        ten = txtTen.getText();
        maNsx = (String) cbo_maNSX.getSelectedItem();
        gia = Double.parseDouble(txtGia.getText());
        soluong = Integer.parseUnsignedInt(txtsoLuong.getText());
        return new QLSanPham(ma, ten, gia, soluong, maNsx);
    }

    void setForm(QLSanPham sp) {
        txtMa.setText(sp.getMasp());
        txtTen.setText(sp.getTensp());
        txtsoLuong.setText(String.valueOf(sp.getSoluongsp()));
        txtGia.setText(String.valueOf(sp.getGiasp()));
        cbo_maNSX.setSelectedItem(sp.getMaNSX());
    }

