    public void fillTable(List<CTHD> list) {
        model = (DefaultTableModel) tblDoanhThu.getModel();
        model.setRowCount(0);
        for (CTHD cthd : list) {
            Object[] row = new Object[]{cthd.getMa(), cthd.getNgayTao(), cthd.getTongTienHD()};
            model.addRow(row);
        }
    }

